def should_send_reminder():
    """–û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ, –Ω—É–∂–Ω–æ –ª–∏ –æ—Ç–ø—Ä–∞–≤–ª—è—Ç—å –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ (–∏—Å–∫–ª—é—á–∞–µ–º –Ω–æ—á–Ω—ã–µ —á–∞—Å—ã)"""
    current_hour = datetime.now(vancouver_tz).hour
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è —Ç–æ–ª—å–∫–æ —Å 7:00 –¥–æ 22:00
    return 7 <= current_hour <= 22


